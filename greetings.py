
def greeting(time_of_day, name):
    print("Good" , time_of_day + ",", name + "!")
    
greeting("morning", "Pope")

#Good"	A string that always stays the same — the first part of the greeting.
#time_of_day + ","	Joins the time of day (like "morning") with a comma → "morning,"
#name + "!"	Joins the name with an exclamation mark → "Pope!"
#print(...)	Puts all the parts together and displays them in the terminal.